package ch11.lecture.p04throw;

public class C01Throw {                       
	                                       //던지는 객체 타입 명시
	public static void main(String[] args) throws Throwable {
		// Throwable 타입의 객체를 던질 수 있음
		Throwable a = new Exception();
		
		throw a;
		
		
		//객체를 던지면 작성했던 메소드에 던진다고 표시를 해줘야 함
		
		
		
		
		
	}
}
